/** Escape HTML special characters to prevent XSS in generated documents. */
function escapeHtml(str: string): string {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;")
}

// ─── Internal helpers ────────────────────────────────────────────────────────

function formatActivity(activity: {
  title: string
  duration: string
  instructions: string
  differentiation?: { support: string; core: string; stretch: string }
  resources?: string[]
}): string[] {
  const lines: string[] = []
  lines.push(`  Title:        ${activity.title}`)
  lines.push(`  Duration:     ${activity.duration}`)
  lines.push(`  Instructions: ${activity.instructions}`)

  if (activity.differentiation) {
    lines.push("  Differentiation:")
    lines.push(`    Support:  ${activity.differentiation.support}`)
    lines.push(`    Core:     ${activity.differentiation.core}`)
    lines.push(`    Stretch:  ${activity.differentiation.stretch}`)
  }

  if (activity.resources && activity.resources.length > 0) {
    lines.push("  Resources:")
    activity.resources.forEach((r) => {
      lines.push(`    - ${r}`)
    })
  }

  return lines
}
