-- ============================================================
-- Helper: recompute confirmed_referral_count for a given affiliate
-- ============================================================

CREATE OR REPLACE FUNCTION public.recount_affiliate_referrals(p_affiliate_id UUID)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_count INTEGER;
BEGIN
  SELECT COUNT(*)::INTEGER
  INTO v_count
  FROM public.affiliate_conversions
  WHERE affiliate_id = p_affiliate_id
    AND status IN ('confirmed', 'paid');

  UPDATE public.affiliate_accounts
  SET confirmed_referral_count = v_count,
      tier = CASE
        WHEN v_count >= 25 THEN 'gold'
        WHEN v_count >= 5 THEN 'silver'
        ELSE 'bronze'
      END
  WHERE id = p_affiliate_id;

  RETURN v_count;
END;
$$;
