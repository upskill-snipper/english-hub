-- Extension for gen_random_uuid() — should already be enabled in production
CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- ------------------------------------------------------------
-- affiliate_accounts — one row per affiliate (applicant or active)
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.affiliate_accounts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,

  -- Public referral code used in ?ref=<code>. Short, URL-safe, unique.
  code TEXT UNIQUE NOT NULL DEFAULT substr(replace(gen_random_uuid()::text, '-', ''), 1, 10),

  -- Applicant details
  full_name TEXT NOT NULL,
  email TEXT NOT NULL UNIQUE,
  website TEXT,
  social_handle TEXT,
  audience_size BIGINT,
  audience_description TEXT NOT NULL,
  promo_strategy TEXT NOT NULL,

  -- Programme state
  status TEXT NOT NULL DEFAULT 'pending'
    CHECK (status IN ('pending', 'approved', 'active', 'suspended', 'terminated', 'rejected')),
  tier TEXT NOT NULL DEFAULT 'bronze'
    CHECK (tier IN ('bronze', 'silver', 'gold')),

  -- Cached counter — recomputed by a trigger/cron. Used for tier-gating fast path.
  confirmed_referral_count INTEGER NOT NULL DEFAULT 0,

  -- Payout details (PII — guarded by RLS)
  payout_method TEXT NOT NULL DEFAULT 'bank_transfer'
    CHECK (payout_method IN ('bank_transfer', 'paypal', 'stripe_connect')),
  payout_email TEXT,
  bank_account_name TEXT,
  bank_sort_code TEXT,
  bank_account_number TEXT,
  stripe_connect_account_id TEXT,

  notes TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  approved_at TIMESTAMPTZ,
  terminated_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_affiliate_accounts_user_id
  ON public.affiliate_accounts(user_id);
CREATE INDEX IF NOT EXISTS idx_affiliate_accounts_status
  ON public.affiliate_accounts(status);
CREATE INDEX IF NOT EXISTS idx_affiliate_accounts_code
  ON public.affiliate_accounts(code);

-- ------------------------------------------------------------
-- affiliate_links — trackable links generated by affiliates
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.affiliate_links (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  affiliate_id UUID NOT NULL REFERENCES public.affiliate_accounts(id) ON DELETE CASCADE,

  -- Short token used in short URLs (/r/<token>)
  token TEXT UNIQUE NOT NULL DEFAULT substr(replace(gen_random_uuid()::text, '-', ''), 1, 8),

  target_path TEXT NOT NULL DEFAULT '/',
  campaign TEXT,
  medium TEXT,
  label TEXT,

  click_count INTEGER NOT NULL DEFAULT 0,
  conversion_count INTEGER NOT NULL DEFAULT 0,

  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  last_clicked_at TIMESTAMPTZ,
  archived_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_affiliate_links_affiliate_id
  ON public.affiliate_links(affiliate_id);
CREATE INDEX IF NOT EXISTS idx_affiliate_links_token
  ON public.affiliate_links(token);

-- ------------------------------------------------------------
-- affiliate_clicks — append-only click log
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.affiliate_clicks (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  affiliate_id UUID NOT NULL REFERENCES public.affiliate_accounts(id) ON DELETE CASCADE,
  link_id UUID REFERENCES public.affiliate_links(id) ON DELETE SET NULL,

  -- Salted hash only — never store raw IPs
  ip_hash TEXT,
  user_agent TEXT,
  referrer TEXT,
  landing_path TEXT,

  -- Resolved attribution metadata for debugging
  country TEXT,
  device TEXT,

  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_affiliate_clicks_affiliate_id
  ON public.affiliate_clicks(affiliate_id);
CREATE INDEX IF NOT EXISTS idx_affiliate_clicks_link_id
  ON public.affiliate_clicks(link_id);
CREATE INDEX IF NOT EXISTS idx_affiliate_clicks_created_at
  ON public.affiliate_clicks(created_at DESC);

-- ------------------------------------------------------------
-- affiliate_conversions — paid conversions linked to an affiliate
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.affiliate_conversions (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  affiliate_id UUID NOT NULL REFERENCES public.affiliate_accounts(id) ON DELETE RESTRICT,
  link_id UUID REFERENCES public.affiliate_links(id) ON DELETE SET NULL,

  -- Idempotency key — typically the Stripe checkout session ID
  external_id TEXT UNIQUE NOT NULL,

  -- Amounts (minor units to avoid floating point)
  order_value_pence BIGINT NOT NULL CHECK (order_value_pence >= 0),
  commission_pence BIGINT NOT NULL CHECK (commission_pence >= 0),
  commission_rate NUMERIC(5,4) NOT NULL CHECK (commission_rate >= 0 AND commission_rate <= 1),
  tier_at_conversion TEXT NOT NULL CHECK (tier_at_conversion IN ('bronze', 'silver', 'gold')),
  currency TEXT NOT NULL DEFAULT 'gbp',

  product_type TEXT CHECK (product_type IN ('subscription', 'one_time', 'course')),
  referred_user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,

  -- Attribution context (snapshot at conversion time)
  attribution_model TEXT NOT NULL DEFAULT 'last-touch'
    CHECK (attribution_model IN ('first-touch', 'last-touch')),
  touched_at TIMESTAMPTZ,

  -- Lifecycle: pending -> confirmed -> paid, or -> voided/refunded
  status TEXT NOT NULL DEFAULT 'pending'
    CHECK (status IN ('pending', 'confirmed', 'paid', 'voided', 'refunded')),
  voided_reason TEXT,

  -- Payout batch linkage (nullable until included in a payout)
  payout_id UUID,

  metadata JSONB,

  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  confirmed_at TIMESTAMPTZ,
  paid_at TIMESTAMPTZ
);

CREATE INDEX IF NOT EXISTS idx_affiliate_conversions_affiliate_id
  ON public.affiliate_conversions(affiliate_id);
CREATE INDEX IF NOT EXISTS idx_affiliate_conversions_link_id
  ON public.affiliate_conversions(link_id);
CREATE INDEX IF NOT EXISTS idx_affiliate_conversions_status
  ON public.affiliate_conversions(status);
CREATE INDEX IF NOT EXISTS idx_affiliate_conversions_external_id
  ON public.affiliate_conversions(external_id);

-- ------------------------------------------------------------
-- affiliate_payouts — one row per payout batch per affiliate
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.affiliate_payouts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  affiliate_id UUID NOT NULL REFERENCES public.affiliate_accounts(id) ON DELETE RESTRICT,

  period_start DATE NOT NULL,
  period_end DATE NOT NULL CHECK (period_end >= period_start),

  conversion_count INTEGER NOT NULL DEFAULT 0,
  amount_pence BIGINT NOT NULL DEFAULT 0 CHECK (amount_pence >= 0),
  currency TEXT NOT NULL DEFAULT 'gbp',

  status TEXT NOT NULL DEFAULT 'calculated'
    CHECK (status IN ('calculated', 'approved', 'paid', 'failed', 'cancelled')),
  payment_method TEXT,
  payment_reference TEXT,

  approved_by TEXT,
  approved_at TIMESTAMPTZ,
  paid_at TIMESTAMPTZ,
  failed_reason TEXT,

  notes TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_affiliate_payouts_affiliate_id
  ON public.affiliate_payouts(affiliate_id);
CREATE INDEX IF NOT EXISTS idx_affiliate_payouts_status
  ON public.affiliate_payouts(status);
CREATE INDEX IF NOT EXISTS idx_affiliate_payouts_period
  ON public.affiliate_payouts(period_start, period_end);

